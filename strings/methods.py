#!/usr/local/bin/python3
# -*- coding: UTF8 -*-

name = "vIkaS NAgiNEnI"

# STR: string/string contained variable
print("Original String \t\t: \t{}".format(name))

# len(STR) function
print("len() of Original String \t: \t{}".format(len(name)))

# STR.lower() function
print("lower() of Original String \t: \t{}".format(name.lower()))

# STR.upper() function
print("upper() of Original String \t: \t{}".format(name.upper()))

# STR.title() function
print("title() of Original String \t: \t{}".format(name.title()))

# STR.count("character") function 
print("count() of Original String \t: \t{}".format(name.count("N")))